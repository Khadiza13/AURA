package com.example.splash;
// Cycle.java

public class Cycle {
    private int startDay;
    private int duration;

    public Cycle(int startDay, int duration) {
        this.startDay = startDay;
        this.duration = duration;
    }

    public int getStartDay() {
        return startDay;
    }

    public int getDuration() {
        return duration;
    }
}
